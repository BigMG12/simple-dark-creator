import type { CategoryId } from "./categories";

/** Legacy union — kept only for back-compat with old imports. New code uses CategoryId. */
export type SpecialtyCategory =
  | "Motivation"
  | "Sales"
  | "Influence"
  | "Leadership"
  | "Storytelling"
  | "Authority";

export type SpeakerSource = "curated" | "imported" | "community";
export type ImportSource = "youtube" | "rumble" | "spotify" | "upload";

export interface Speaker {
  id: string;
  name: string;
  monogram: string;
  specialty: string;
  /** New canonical category id */
  category: CategoryId;
  signatureTrait: string;
  bio: string;
  idealWpm: [number, number];
  pauseFrequency: "low" | "medium" | "high";
  energyProfile: string;
  famousSpeeches: { title: string; url: string }[];
  learnings: string[];
  /** 3-4 specific stylistic patterns this speaker uses */
  styleTraits: string[];
  /** Topics they speak about most */
  commonThemes: string[];
  /** One-liner: "...if you want to learn X" */
  perfectFor: string;
  source: SpeakerSource;
  /** Only present when source !== 'curated' */
  importedFrom?: ImportSource;
  /** Minutes of audio analyzed (imported speakers only) */
  transcribedMinutes?: number;
  /** Sort order — higher = surfaces earlier */
  sortOrder?: number;
  /** ISO date — used by "Recently Added" sort */
  addedAt?: string;
  /** Number of community sessions trained against this speaker — used for "Most Used" sort */
  communityUseCount?: number;
  /** Extended persona fields for personalized feedback */
  persona?: {
    favoriteWords?: string[];
    verbalTics?: string[];
    rhetoricalDevices?: string[];
    hates?: string[];
    perfectForNotFor?: { perfectFor: string; notFor: string };
    closestMentors?: string[];
    oppositeOf?: string[];
    sampleFeedback?: string;
  };
}

