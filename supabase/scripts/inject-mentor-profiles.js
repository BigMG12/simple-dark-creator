#!/usr/bin/env node
/**
 * Script to inject 12-layer mentor profiles into Supabase
 * Reads JSON files and updates speakers table
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Mentor mappings: filename -> speaker name -> category
const MENTORS = [
  { file: '01_belfort.json', name: 'Jordan Belfort', category: 'sales' },
  { file: '02_voss.json', name: 'Chris Voss', category: 'influence' },
  { file: '03_hormozi.json', name: 'Alex Hormozi', category: 'sales' },
  { file: '04_robbins.json', name: 'Tony Robbins', category: 'motivation' },
  { file: '05_goggins.json', name: 'David Goggins', category: 'motivation' },
  { file: '06_jobs.json', name: 'Steve Jobs', category: 'leadership' },
  { file: '07_obama.json', name: 'Barack Obama', category: 'leadership' },
  { file: '08_sinek.json', name: 'Simon Sinek', category: 'storytelling' },
  { file: '09_peterson.json', name: 'Jordan Peterson', category: 'authority' },
  { file: '10_tate.json', name: 'The Top G', category: 'sales' }
];

const MENTORS_DIR = path.join(__dirname, '../seeds/mentors_v2');

async function main() {
  console.log('═══════════════════════════════════════════════════════');
  console.log('DEEP MENTOR DNA v2 INJECTION');
  console.log('═══════════════════════════════════════════════════════\n');

  await generateSQL();
}

async function generateSQL() {
  console.log('Mode: Generating SQL migration file\n');

  const sqlParts = [];

  sqlParts.push(`-- ============================================================
-- BIG SPEAKING — Deep Mentor DNA v2 — Injection
-- Migration: 051_deep_mentor_dna_v2_inject.sql
-- Generated by inject-mentor-profiles.js
-- ============================================================
`);

  for (const mentor of MENTORS) {
    const filePath = path.join(MENTORS_DIR, mentor.file);

    console.log(`Processing: ${mentor.name}...`);

    if (!fs.existsSync(filePath)) {
      console.error(`  ❌ File not found: ${filePath}`);
      continue;
    }

    const profileJson = fs.readFileSync(filePath, 'utf8');
    const profile = JSON.parse(profileJson);

    // Escape single quotes for SQL
    const escapedJson = JSON.stringify(profile).replace(/'/g, "''");

    sqlParts.push(`
-- ============================================================
-- ${mentor.name}
-- ============================================================

UPDATE public.speakers
SET
  persona_profile = '${escapedJson}'::jsonb,
  persona_version = 2,
  category_id = (SELECT id FROM public.speaker_categories WHERE name = '${mentor.category}')
WHERE name = '${mentor.name}';
`);

    console.log(`  ✅ Added (${profileJson.length} chars)`);
  }

  // Add verification
  sqlParts.push(`
-- ============================================================
-- Verification
-- ============================================================

DO $$
DECLARE
  updated_count INT;
  profile_lengths TEXT;
BEGIN
  SELECT COUNT(*) INTO updated_count
  FROM public.speakers
  WHERE persona_version = 2;

  SELECT string_agg(
    name || ': ' || length(persona_profile::text) || ' chars',
    E'\\n' ORDER BY name
  ) INTO profile_lengths
  FROM public.speakers
  WHERE persona_version = 2;

  RAISE NOTICE '═══════════════════════════════════════════════════════';
  RAISE NOTICE 'DEEP MENTOR DNA v2 INJECTION COMPLETE';
  RAISE NOTICE '═══════════════════════════════════════════════════════';
  RAISE NOTICE 'Updated speakers: %', updated_count;
  RAISE NOTICE '';
  RAISE NOTICE 'Profile lengths:';
  RAISE NOTICE '%', profile_lengths;
  RAISE NOTICE '═══════════════════════════════════════════════════════';
END $$;
`);

  const outputPath = path.join(__dirname, '../migrations/051_deep_mentor_dna_v2_inject.sql');
  fs.writeFileSync(outputPath, sqlParts.join('\n'));

  console.log('\n═══════════════════════════════════════════════════════');
  console.log(`✅ SQL migration generated: ${outputPath}`);
  console.log(`   File size: ${fs.statSync(outputPath).size} bytes`);
  console.log('═══════════════════════════════════════════════════════');
}

main().catch(err => {
  console.error('Error:', err);
  process.exit(1);
});
